public class Increments 
{
	public static void main(String[] args)
	{
			int count = 0;
			System.out.println("count= " + count);
			System.out.println("count++= " + (count++));
			System.out.println("count= " + count);
			System.out.println("++count= " + (++count));
			System.out.println("count= " + count);
	}
}